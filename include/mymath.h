#ifndef MYMATH_H_
#define MYMATH_H_

/**
 * Square root function.
 * @param x The value to calculate the square root for.
 */
int sqrt(int x);

/**
 * Calculate the factorial.
 * @param n The parameter to calculate the factorial for.
*/
double fact(double n);

/**
 * Raise a number to a power.
 * @param n The base number.
 * @param power The power to raise to.
 */
double pow(double n, double power);

/**
 * Calculate the flating-point remainder of the division operation.
 * @param a The numerator.
 * @param b The denominator.
 */
double fmod(double a, double b);

/**
 * Calculate the sine of an input value.
 * @param x The value to calculate the sine for.
 */
double sin(double x);

#endif  // MYMATH_H_
